package com.oldmountain.dzm.entity;

public class TeamBuffTemplate {
	private int id;
	private String name;
	private int increaseType;
	private int unlockLevel;
	private int initLevel;
	private int increaseLevel;
	private String icon;
	private String smallIcon;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getIncreaseType() {
		return increaseType;
	}
	public void setIncreaseType(int increaseType) {
		this.increaseType = increaseType;
	}
	public int getUnlockLevel() {
		return unlockLevel;
	}
	public void setUnlockLevel(int unlockLevel) {
		this.unlockLevel = unlockLevel;
	}
	public int getInitLevel() {
		return initLevel;
	}
	public void setInitLevel(int initLevel) {
		this.initLevel = initLevel;
	}
	public int getIncreaseLevel() {
		return increaseLevel;
	}
	public void setIncreaseLevel(int increaseLevel) {
		this.increaseLevel = increaseLevel;
	}
	public String getIcon() {
		return icon;
	}
	public void setIcon(String icon) {
		this.icon = icon;
	}
	public String getSmallIcon() {
		return smallIcon;
	}
	public void setSmallIcon(String smallIcon) {
		this.smallIcon = smallIcon;
	}
	

}
